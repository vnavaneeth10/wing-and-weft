export const getWhatsAppLink = (product?: Product): string => {
  const base = `https://wa.me/${WHATSAPP_NUMBER}`;
  if (!product) return `${base}?text=${encodeURIComponent('Hi! I need help choosing a saree.')}`;
  const text = `Hi! I'm interested in:\n*${product.name}* (ID: ${product.id})\nCategory: ${product.category.replace('-', ' ')}\nPrice: ₹${product.discountPrice || product.price}\nFabric: ${product.fabric}\n\nCould you please help me with this product?`;
  return `${base}?text=${encodeURIComponent(text)}`;
};

